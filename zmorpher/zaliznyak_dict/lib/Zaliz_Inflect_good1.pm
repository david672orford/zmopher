    ($s2=~/^к/) && ($p2=~/^ч.д/) && (@wfh = map { ($_->{'ф'} = "фн"); $_ } @wfh);
    return \@wfh;
  }

  # currently exceptions not implemented
  if (!defined $i) { $wf{'с'} = $wi{'с'}; $wf{'у'} = $wi{'у'}; return [\%wf] }

  # $ae - ударное окончание: "у" - ударное, "б" - безударное (по умолчанию)
