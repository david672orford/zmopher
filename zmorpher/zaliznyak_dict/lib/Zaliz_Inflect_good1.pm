    }
    $wb = ($i == 13) && $wi{'сби'} || $wi{'сб3'};
    $we = ($wb =~ /[^$sch]$/) && "я" || "а";
    ($i == 13) && ($ac = $a) || ($ae = "у");
  }
  # Действительное причастие прошедшего времени
  elsif ($p eq "чпд") {
    # ($wb, $ac) = @{$wi{"sfпем"} || (inflect($wi, "пем") && $wi->{"sfпем"})};
    my $wf2 = $wi{"swfпем"} || (inflect($wi, "пем") && $wi->{"swfпем"});
