#          (($os !~ /6/) && (warn "5 without 6 is impossible!"))));
#          (($os !~ /5/ || $p ne "пем") && ($wb =~ s/ну$//)));
    my $u2 = $wi{'у2'};
    (defined $u2) && ($u1 = $u2,1) || ($u1 = "a");
