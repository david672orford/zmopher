  my ($ae, $ac) = ("б");

  # Личные формы настоящего времени несов.вида и буд.вр. сов.вида
  if ($p =~ /^[нб](..)$/) {
    my $p2 = $1;
    $wb = ($p2 eq "1е" || $p2 eq "3м" && $wi{'тс'} == 1) && $wi{'сб1'} || $wi{'сб3'};
       $p2 eq "1е" && ($we = $wi{'ок1'}, $ae = "у")
    || $p2 eq "2е" && ($we = $wi{'ок3е'}."шь")
    || $p2 eq "3е" && ($we = $wi{'ок3е'}."т")
