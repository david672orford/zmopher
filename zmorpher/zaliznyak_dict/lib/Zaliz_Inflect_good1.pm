       ($wi1{'у2'} = "b") && (push(@wfh, @{inflect(\%wi1, $p)})) ||
    ($u1 eq "b1" && $p eq "кимм") &&
       ($wi1{'у2'} = "a") && (unshift(@wfh, @{inflect(\%wi1, $p)}));
  }

  \@wfh
