            ($h{'у'} = defined($excp1->[$j]) && $excp1->[$j]->{'у'} || $excp1->[0]->{'у'});
          !(($excpw3 =~ /ит(с[ья])?$/) ||
            ($excpw3 =~ /[$v]ет(с[ья])?$/) &&
