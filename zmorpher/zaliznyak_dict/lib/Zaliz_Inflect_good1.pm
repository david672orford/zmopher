    }
    my %wf2 = (defined $wfh[1]) ? %{$wfh[1]} : %{$wfh[0]}; # %wf
    (($i == 9) && (!defined $wi{'ся'}) && $sv) &&
      (@wf2{'с','у'} = ($wi{'с'},$wi{'у'})) &&
      ($wf2{'с'} =~ s/ть$/в/) && unshift(@wfh, \%wf2);
