    my $i = 1;
    for $wfhi (@wfh) {
      if ($wfhi->{'с'} =~ /ее$/) {
        my %wf1 = %{$wfhi};
        $wf1{'с'} =~ s/е$/й/ && splice(@wfh,$i,0,\%wf1);
      } $i++;
    }
  }

  if ($p =~ /^к/ && $u1 =~ m([12])) {
