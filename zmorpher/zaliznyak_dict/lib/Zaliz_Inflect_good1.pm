  }

  if ($u1 =~ /c[12]/ && ($p =~ /^п[ем][мжс]?$/)) {
    my %wf1 = %wf;
    (($u1 eq "c1") && ($p eq "пес") ||
     ($u1 eq "c2") && ($p =~ /^п[ем][мс]?$/)) &&
       ($wf1{'с'} =~ /[$v][^$v]*$/) && ($wf1{'у'} = length($`) + 1) &&
         (($p eq "пем") && ($wf1{'з'} = "устар.") || 1) && push(@wfh, \%wf1)
  }

  # save word forms
  ($p eq "пем" || $p =~ /^ч/) && ($wi->{"swf$p"} = \@wfh); # $p eq "чпд"

  # исключение для "пем"
  if (defined($exc = $wi{'искл'})) {
    if (($p eq "пем") && defined($excp = $exc->{$p})) {
      return $excp;
    }
  }

  \@wfh
}


