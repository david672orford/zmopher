        ($ac >= length($wb)) && ($wb =~ /[$v][^$v]*?$/) && ($ac = length($`) + 1)
    } else {
         ($we =~ /[$v]/)         && ($ac = length($wb) + length($`) + 1)
      || ($wb =~ /[$v][^$v]*?$/) && ($ac = length($`) + 1);
         ($we =~ s/^е/ё/);
      ($p eq "чнс") && ($wb =~ /[$c]$/) && ($we =~ s/^ё/о/)
    }
  }

  $wb .= $we;

  ($os =~ /1/) && ($p =~ /^п([ем][мс]?)$/ || ($p =~ /^ч.с$/)) &&
    (substr($wb,0,$ac-1) =~ /[$v][^$v]*$/) && ($ac = length($`) + 1);
  ($os =~ /4/) && ($p eq "чнд") &&
    (substr($wb,0,$ac-1) =~ /[$v][^$v]*$/) && ($ac = length($`) + 1);

