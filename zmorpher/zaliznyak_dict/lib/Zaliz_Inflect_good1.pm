    ($os =~ /3/) && ($we = (($p eq "!2е") && "и" || "ь"));
    ($osf =~ /3/) && (($p eq "!2е") && ($os !~ /3/) || ($p eq "!2м")) && ($we = "ь");
  }
  # Действительное причастие настоящего времени
  elsif ($p eq "чнд") {
    if (!$nsv) { $wf{'ф'} = "фн" }
    $wb = $wi{'сб3'};
    $we = $wi{'ок3м'}."щий";
    $ae = "у" if $wi{'тс'} == 2;
  }
  # Деепричастие настоящего времени
  elsif ($p eq "дн") {
    if (!($nsv && $i =~ /^([124-7]|1[0236])$/)) {
      # $wf{'с'} = ""; $wf{'у'} = 0; return [\%wf];
